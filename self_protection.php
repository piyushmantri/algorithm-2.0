                    <li>&nbsp;- Check, from time to time, the state of the roof, the building's down spouts and the nearby drains.
                    <li>&nbsp;- Place important documents and, especially, hazardous products, in those parts of the house where the risk of their deteriorating due to moisture or their spilling is reduced.
                    <li>&nbsp;- Do not park vehicles or camp in dry riverbeds or on river banks, to avoid being surprised by a sudden rise in water level or a torrent.
